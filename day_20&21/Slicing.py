#slicing works for tuple aswell

piano_keys = ["a","b","c","d","e","f","g"]

print(piano_keys[2:5]) #sliced list from piano_keys list from 2 to 4

print(piano_keys[2:]) #slice from any position to end of the list

print(piano_keys[:5]) # slice every thing  after position 4

print(piano_keys[2:5:2]) # skip every second element in sliced list

print(piano_keys[::2]) # skip every second element in the list

print(piano_keys[::-1]) #reverse the list

